//calculation.js

var calc = {
  add: function(a, b) {
    let x = a + b;
    return x;
  },
  
  subtract: function(a, b) {
    let x = a - b;
    return x;
  },
  
  multiply: function(a, b) {
    let x = a * b;
    return x;
  },
  
  divide: function(a, b) {
    let x = a / b;
    return x;
  }

}
module.exports = calc;